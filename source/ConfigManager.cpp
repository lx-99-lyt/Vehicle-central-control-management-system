std::string trim(const std::string& s) {
    const std::string spaces = " \t\r\n";
    const auto begin = s.find_first_not_of(spaces);
    if (begin == std::string::npos) {
        return "";
    }
    const auto end = s.find_last_not_of(spaces);
    return s.substr(begin, end - begin + 1);
}

bool writeAll(int fd, const char* data, size_t size) {
    size_t written = 0;
    while (written < size) {
        const ssize_t n = ::write(fd, data + written, size - written);
        if (n <= 0) {
            return false;
        }
        written += static_cast<size_t>(n);
    }
    return true;
}

uint8_t parseGear(const std::string& text) {
    if (text == "P") return static_cast<uint8_t>(Car::Gear::P);
    if (text == "R") return static_cast<uint8_t>(Car::Gear::R);
    if (text == "N") return static_cast<uint8_t>(Car::Gear::N);
    if (text == "D") return static_cast<uint8_t>(Car::Gear::D);
    return static_cast<uint8_t>(Car::Gear::P);
}

